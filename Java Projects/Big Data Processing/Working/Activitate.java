package Working;

public class Activitate {
	private int repetari;
	private String activitate;
	
	public Activitate(int repetari, String activitate) {
		this.repetari = repetari;
		this.activitate = activitate;
	}

	public int getRepetari() {
		return repetari;
	}

	public void setRepetari(int repetari) {
		this.repetari = repetari;
	}

	public String getActivitate() {
		return activitate;
	}

	public void setActivitate(String activitate) {
		this.activitate = activitate;
	}
	
	
	
}
